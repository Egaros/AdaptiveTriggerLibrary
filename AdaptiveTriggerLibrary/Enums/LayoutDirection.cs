namespace AdaptiveTriggerLibrary.Enums
{
    /// <summary>
    /// Defines constants that specify a layout direction for the UI.
    /// </summary>
    public enum LayoutDirection
    {
        /// <summary>
        /// Layout direction is left to right.
        /// </summary>
        LTR = 0,
        /// <summary>
        /// Layout direction is right to left.
        /// </summary>
        RTL = 1,
        /// <summary>
        /// Layout direction is top to bottom and left to right.
        /// </summary>
        TTBLTR = 2,
        /// <summary>
        /// Layout direction is top to bottom and right to left.
        /// </summary>
        TTBRTL = 3
    }
}